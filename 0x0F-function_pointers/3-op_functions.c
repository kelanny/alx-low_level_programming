#include "3-calc.h"

/**
 * op_add - Returns a sum of two integers
 * @a: First integer
 * @b: Second integer
 *
 * Return: Sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtract two integers
 * @a: First integer
 * @b: Second integer
 *
 * Return: Difference of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiply two integers
 * @a: First integer
 * @b: Second integer
 *
 * Return: product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divide two integers
 * @a: First integer
 * @b: Second integer
 *
 * Return: A Division of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Finds a modulo of two integers
 * @a: First integer
 * @b: Second integer
 *
 * Return: A remaider of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
